Route::get('/', function () {
    return view('welcome');
});

Route::middleware([
    'auth:sanctum',
    config('jetstream.auth_session'),
    'verified',
])->group(function () {
    Route::get('/dashboard', function () {
        return view('dashboard');
    })->name('dashboard');
});

Route::get('/notification', function () {
    return view('notification');
});
Route::post('/notification', function () {
    $name = request()->name;

    event(new WebSocketEvent($name));
});